#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void			*ret;

	ret = NULL;
	ret = malloc(size * nmemb);
	if (!ret)
		return (NULL);
	ft_bzero(ret, size * nmemb);
	return (ret);
}
