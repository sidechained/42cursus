#include "../inc/libft.h"

int	ft_isalnum(int c)
{
	unsigned char	str;

	str = c;
	if ((str >= '0' && str <= '9')
		|| (str >= 'A' && str <= 'Z')
		|| (str >= 'a' && str <= 'z'))
		return (1);
	else
		return (0);
}
